async function upsertDocument(document) {
  const existing = await sql`
    SELECT id FROM report_templates WHERE slug = ${document.slug} LIMIT 1
  `;

  if (existing.length > 0) {
    await sql`
      UPDATE report_templates
      SET body = ${document.body}
      WHERE slug = ${document.slug}
    `;

    console.log(`🔄 본문 업데이트: ${document.slug}`);
    return;
  }

  await sql`
    INSERT INTO report_templates (
      slug,
      title,
      category,
      report_category,
      description,
      version,
      body,
      published,
      "order"
    )
    VALUES (
      ${document.slug},
      ${document.title},
      ${document.category},
      ${document.reportCategory},
      ${document.description},
      ${document.version},
      ${document.body},
      true,
      ${document.order}
    )
  `;

  console.log(`✅ 신규 삽입: ${document.slug}`);
}

